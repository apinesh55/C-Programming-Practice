#include <stdio.h>
#include <math.h>

int main()
{
    printf("Squre root: %0.2f\n",sqrt(25));
    printf("Power: %0.2f\n",(2,10));
    printf("Absolute value of -100: %d\n",abs(-100));
    printf("Ceiling: %0.2f\n",ceil(4.8));
    printf("Ceiling: %0.2f\n",ceil(4.2));
    printf("Floor: %0.2f\n",floor(4.8));
    printf("Floor: %0.2f\n",floor(4.2));
    printf("Round: %0.2f\n",round(4.8));
    printf("Round: %0.2f\n",round(4.2));
    
    return 0;
}