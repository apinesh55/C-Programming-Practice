// Relational operators
#include <stdio.h>
int main()
{
    int a=10,b=5;

    printf("less than               : %d",a<b);
    printf("\ngrater than             : %d",a>b);
    printf("\nequal to                : %d",a==b);
    printf("\ngrater than or equal    : %d",a>=b);
    printf("\nless than or equal      : %d",a<=b);
    printf("\nnot equal to            : %d",a!=b);

    return 0;
}